package com.hhwork.model;

import com.hhwork.annotation.Table;

@Table(value="basetype")
public class BaseType {
	private int id;
	
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
